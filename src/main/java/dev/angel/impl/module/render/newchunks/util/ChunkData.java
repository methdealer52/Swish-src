package dev.angel.impl.module.render.newchunks.util;

public class ChunkData {
    private int x;
    private int z;

    public ChunkData(int x, int z) {
        this.x = x;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
